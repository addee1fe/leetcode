// from go sources
func hammingWeight(x uint32) int {
	return int(pop8tab[x>>24] + pop8tab[x>>16&0xff] + pop8tab[x>>8&0xff] + pop8tab[x&0xff])
}

func hammingWeightLoop(num uint32) int {
	var sum int
	for num != 0 {
		sum++
		num &= (num - 1)
	}
	return sum
}
